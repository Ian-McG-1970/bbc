.CALC_LANDSCAPE_CHAR_PIXEL ; convert 0-1023 to 0-3 pixel pos + 0-255 map pos
  LDA MAP_POS +1
  LSR A ; SHIFT HI LSB INTO CARRY
  LDA MAP_POS
  ROR A ; ROTATE CARRY INTO MSB AND PUT LSB INTO CARRY
  BCC left

	STA MAP_LFT_LSB_ADDR
	STA MAP_LFT_MSB_ADDR
  CLC
  adc #1
	STA MAP_RGT_LSB_ADDR
	STA MAP_RGT_MSB_ADDR

				LDX #>MAP_LFT_LSB
				STX MAP_RGT_LSB_ADDR+1
				LDX #>MAP_LFT_MSB
				STX MAP_RGT_MSB_ADDR+1
				LDX #>MAP_RGT_LSB
				STX MAP_LFT_LSB_ADDR+1
				LDX #>MAP_RGT_MSB
				STX MAP_LFT_MSB_ADDR+1
  RTS

.left
	STA MAP_LFT_LSB_ADDR
	STA MAP_LFT_MSB_ADDR
	STA MAP_RGT_LSB_ADDR
	STA MAP_RGT_MSB_ADDR

				LDX #>MAP_LFT_LSB
				STX MAP_LFT_LSB_ADDR+1
				LDX #>MAP_LFT_MSB
				STX MAP_LFT_MSB_ADDR+1
				LDX #>MAP_RGT_LSB
				STX MAP_RGT_LSB_ADDR+1
				LDX #>MAP_RGT_MSB
				STX MAP_RGT_MSB_ADDR+1

  RTS

